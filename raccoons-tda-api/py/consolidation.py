def print_upper_keys():
    combined = {}

    for m in maps:
        for k in m:
            combined[k] = ""

    for k in combined.keys():
        uppered = k.upper()
        print(f"public static final String {uppered} = \"{k}\";")


def print_exclusive():
    for i in range(0, len(maps)):
        m = maps[i]
        exclusive_keys = {}
        for k in m.keys():
            for n in maps:
                eligible = True
                if k in n.keys():
                    eligible = False
                if eligible:
                    exclusive_keys[k] = ""
        print(str(i) + " -> " + str(exclusive_keys))


def print_common():
    flatten_all = {}
    common = {}

    for m in maps:
        for k, v in m.items():
            flatten_all[k] = True

    for k in flatten_all.keys():
        eligible = True
        for m in maps:
            if k not in m.keys():
                eligible = False
        if eligible:
            common[k] = ""

    print(common)


